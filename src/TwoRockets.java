class RocketAndRocket {

        public static void main(String[] args) {
            Triangle();
            Square();
            USA();
            Square();
            Triangle();
        }
        public static void Triangle() {
            System.out.println("   /\\       /\\");
            System.out.println("  /  \\     /  \\");
            System.out.println(" /    \\   /    \\");
        }
        public static void Square() {
            System.out.println("+------+ +------+");
            System.out.println("|      | |      |");
            System.out.println("|      | |      |");
            System.out.println("+------+ +------+");
        }
        public static void USA() {
            System.out.println("|United| |United|");
            System.out.println("|States| |States|");
        }
    }

